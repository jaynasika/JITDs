package jitd.mfa;

public class FrequencyException extends Exception {

    /**
     * Creates a new instance of
     * <code>FrequencyException</code> without detail message.
     */
    public FrequencyException() {
    }

    /**
     * Constructs an instance of
     * <code>FrequencyException</code> with the specified detail message.
     *
     * @param msg the detail message.
     */
    public FrequencyException(String msg) {
        super(msg);
    }
}
